if (isset($_POST['SendEmail'])) {
    $Name = $_POST['Name'];
    $Email = $_POST['Email'];
    $Subject = $_POST['Subject'];
    $Content = $_POST['comment'];
    //PHPMailer Object
    $mail = new PHPMailer(true); //Argument true in constructor enables exceptions
    
    //From email address and name
    $mail->From = $Email;
    $mail->FromName = $Name;
    
    //To address and name
    $mail->addAddress("smartmine227@gmail.com", "Recepient Name");
    $mail->addAddress("smartmine227@gmail.com"); //Recipient name is optional
    
    //Address to which recipient will reply
    $mail->addReplyTo("smartmine227@gmail.com", "Reply");
    
    //Send HTML or Plain Text email
    $mail->isHTML(true);
    
    $mail->Subject = $Subject;
    $mail->Body = "<i>".$Content."</i>";
    $mail->AltBody = "This is the plain text version of the email content";

    try {
        $mail->send();
        echo '<script src="assets/js/vendor_form.js" type="text/javascript"></script>';
        $sessionValue = 'false';
    } catch (Exception $e) {
        echo "Mailer Error: " . $mail->ErrorInfo;
